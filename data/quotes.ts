/**
 * دالة محسنة لجلب اقتباس عشوائي
 * تستخدم اليوم والشهر والسنة لضمان تغيير الاقتباس يومياً 
 * حتى لو تجاوز عدد الاقتباسات 31
 */
export const getRandomQuote = () => {
  const now = new Date();
  // نجمع اليوم مع الشهر للحصول على رقم متغير يتجاوز الـ 31
  const seed = now.getDate() + (now.getMonth() * 31);
  const index = seed % QUOTES.length;
  return QUOTES[index];
};

/**
 * إذا كنت تريد عشوائية حقيقية في كل مرة يتم فيها تحميل الصفحة
 * استخدم هذه الدالة بدلاً من السابقة:
 */
export const getTrueRandomQuote = () => {
  const index = Math.floor(Math.random() * QUOTES.length);
  return QUOTES[index];
};